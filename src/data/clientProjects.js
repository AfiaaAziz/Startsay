// Helper function to get project by slug
export const getClientProjectBySlug = (slug) => {
  return clientProjects[slug] || null;
};

// Helper function to get all project slugs
export const getAllClientProjectSlugs = () => {
  return Object.keys(clientProjects);
};

// Helper function to get next/previous project
export const getAdjacentClientProjects = (slug) => {
  const slugs = getAllClientProjectSlugs();
  const currentIndex = slugs.indexOf(slug);

  return {
    previous:
      currentIndex > 0 ? clientProjects[slugs[currentIndex - 1]] : null,
    next:
      currentIndex < slugs.length - 1
        ? clientProjects[slugs[currentIndex + 1]]
        : null,
  };
};
